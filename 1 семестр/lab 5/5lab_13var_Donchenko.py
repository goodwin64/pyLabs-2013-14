# 13) Написати програму знаходження суми n
# перших членів послідовності 1/2+3/4+5/6+...

print ('Дана послідовність 1/2+3/4+5/6+...')
n_max=int(input('Введіть кількість N перших членів для знаходження їхньої суми: '))
S=0
a=1
b=2

for n in range (n_max):
    S+=a/b
    a+=2
    b+=2

print ('Сумма', n_max, 'перших членів прогресії дорівнює', S)
